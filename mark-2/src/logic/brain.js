// 3. Функция для извлечения JSON-строки из ответа модели, удаляя возможные обертки и префиксы
const extractJsonString = (raw) => { // Функция для извлечения JSON-строки из ответа модели, удаляя возможные обертки и префиксы
  if (!raw || typeof raw !== 'string') return raw; // Если входные данные не строка, возвращаем их как есть

  let cleaned = raw 
    .replace(/```(?:json|javascript|js|bash|txt)?/gi, '') // Удаляем возможные markdown обертки ```json, ```javascript, ```js, ```bash, ```txt и просто ```
    .replace(/[\u200B-\u200D\uFEFF]/g, '') // Удаляем невидимые символы типа zero-width space
    .trim(); // Удаляем лишние пробелы в начале и конце

  // Удаляем возможные префиксы типа "javascript" или "json"
  cleaned = cleaned.replace(/^\s*(javascript|json)\s*\n?/i, '').trim();

  // Найдем самое первое и последнее вхождение JSON-объекта или массива
  const firstBrace = cleaned.indexOf('{'); // Начало JSON-объекта
  const lastBrace = cleaned.lastIndexOf('}'); // Конец JSON-объекта
  const firstBracket = cleaned.indexOf('['); // Начало JSON-массива
  const lastBracket = cleaned.lastIndexOf(']'); // Конец JSON-массива

  if (firstBrace !== -1 && lastBrace !== -1 && firstBrace < lastBrace) { // Если найден JSON-объект, извлекаем его
    cleaned = cleaned.slice(firstBrace, lastBrace + 1); // Включаем последний символ }
  } else if (firstBracket !== -1 && lastBracket !== -1 && firstBracket < lastBracket) { // Если найден JSON-массив, извлекаем его
    cleaned = cleaned.slice(firstBracket, lastBracket + 1); // Включаем последний символ ]
  }

  return cleaned.trim(); // Возвращаем очищенную строку, готовую для парсинга JSON
};
